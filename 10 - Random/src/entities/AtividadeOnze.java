package entities;

public class AtividadeOnze {
	private double altura;
	private char genero;

	public AtividadeOnze(double altura, char genero) {
		super();
		this.altura = altura;
		this.genero = genero;
	}

	public double getAltura() {
		return altura;
	}

	public char getGenero() {
		return genero;
	}

}
